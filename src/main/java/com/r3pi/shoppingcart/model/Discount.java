package com.r3pi.shoppingcart.model;

import java.math.BigDecimal;

public class Discount {
    private String name;
    private BigDecimal value;

    public Discount(String name, BigDecimal value) {
        this.name = name;
        this.value = value;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BigDecimal getValue() {
        return value;
    }

    public void setValue(BigDecimal value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Discount discount = (Discount) o;

        if (name != null ? !name.equals(discount.name) : discount.name != null) return false;
        return value != null ? value.equals(discount.value) : discount.value == null;
    }

    @Override
    public int hashCode() {
        int result = name != null ? name.hashCode() : 0;
        result = 31 * result + (value != null ? value.hashCode() : 0);
        return result;
    }
}
